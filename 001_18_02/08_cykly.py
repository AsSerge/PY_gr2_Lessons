# Программа 03.03 - Стандартное отклонение
import math
l = [12,34,45,56,67,89,67,56,45,78,89,23,13,34,56,34,45,75,11]
s = sum(l) / len(l) # Среднеарифметическое последовательности
count = 0
for k in l:
    count = count + (k - s) ** 2
    
d = math.sqrt(count/(len(l)-1))

print ("Последовательность:\t", (l))
print ("Среднеарифметическое:\t", s)
print ("Стандартное отклонение:\t", d)